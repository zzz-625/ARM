//第三步：将Flash里的HDCP Key读出来保存
void ReadKey(void)
{
	int bRet;
#ifdef LT7911_UPGRADE_DEBUG
	printf("ReadKey \n");
#endif
	LT_WriteI2C_Byte( 0xFF, 0x80 );
	LT_WriteI2C_Byte( 0xEE, 0x01 );

	//读取之前reset fifo
	LT_WriteI2C_Byte( 0xFF, 0x90 );
	uint8_t bRead = LT_ReadI2C_Byte( 0x02 );
	bRead &= 0xDF;
	LT_WriteI2C_Byte( 0x02, bRead );
	bRead |= 0x20;
	LT_WriteI2C_Byte( 0x02, bRead );
	
	//wren enable
	LT_WriteI2C_Byte( 0xFF, 0x80 );
	LT_WriteI2C_Byte( 0x5A, 0x86 );
	LT_WriteI2C_Byte( 0x5A, 0x82 );
	
	long lReadAddr = 0x6000;//Key存储在Flash里的地址是0x6000
	uint8_t addr[3] = { 0, 0, 0 };
	addr[0] = ( lReadAddr & 0xFF0000 ) >> 16;
	addr[1] = ( lReadAddr & 0xFF00 )   >>  8;
	addr[2] = lReadAddr & 0xFF;

	int nPage = 286/ 16;//Key总共286个字节，配置一次最多读写16个字节
	if ( 286 % 16 != 0 )
	{
		++nPage;
	}

	for ( int i=0; i<nPage; ++i )//每次最多读16个字节，总共读npage次
	{
		//设置读取地址
		LT_WriteI2C_Byte( 0x5E, 0x60 | 0xF );
		LT_WriteI2C_Byte( 0x5A, 0xA2 );
		LT_WriteI2C_Byte( 0x5A, 0x82 );
		LT_WriteI2C_Byte( 0x5B, addr[0] );
		LT_WriteI2C_Byte( 0x5C, addr[1] );
		LT_WriteI2C_Byte( 0x5D, addr[2] );
		LT_WriteI2C_Byte( 0x5A, 0x92 );
		LT_WriteI2C_Byte( 0x5A, 0x82 );	
		LT_WriteI2C_Byte( 0x58, 0x01 );

		int nPageReadLen = 16;
		if ( 286 - i * 16 < 16)//最后一次长度没有16个，计算最后一次的数据长度
		{
			nPageReadLen = 286 - i * 16;//nPageReadLen是最后一次的长度
		#ifdef LT7911_UPGRADE_DEBUG
			printf("nPageReadLen = %d \n",nPageReadLen);
		#endif
		}
		
		//将读到的Key数据储存起来
		uint8_t byPageReadData[128];
		memset(byPageReadData, 0, 128);
		bRet = LT_ReadI2C_ByteN( 0x5F, byPageReadData, nPageReadLen );//从0x5F中读取
		printf("bRet = %d \n",bRet);
		for (int m = 0;m < nPageReadLen; m++)
		{
			memset(Key_Data+(i*16)+m, byPageReadData[m], 1);
		#if 0	//sdef LT7911_UPGRADE_DEBUG
			printf("keydata[%d] = %x, readdata[%d] = %x \n",(i*16+m),Key_Data[(i*16)+m],m,byPageReadData[m]);
		#endif
		}
		
		
		//下次读取时地址增加16个
		lReadAddr += nPageReadLen;
		addr[0] = ( lReadAddr & 0xFF0000 ) >> 16;
		addr[1] = ( lReadAddr & 0xFF00 )   >>  8;
		addr[2] = lReadAddr & 0xFF;		
	}
}

