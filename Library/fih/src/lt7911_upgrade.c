//第一步：检查chip id，查看I2c是否接通
int CheckChipId(void)
{
#ifdef LT7911_UPGRADE_DEBUG
	printf("CheckChipId \n");
#endif
	LT_WriteI2C_Byte(0xFF, 0x80);
	LT_WriteI2C_Byte(0xEE, 0x01);

	LT_WriteI2C_Byte(0xff, 0xa0);
	uint8_t bA000 = LT_ReadI2C_Byte(0x00);
	uint8_t bA001 = LT_ReadI2C_Byte(0x01);

	int nChipID = ((bA000 << 8) | bA001);
	
	printf("CheckChipId  0x%x \n",nChipID);
	
	return nChipID == 0x1605;
}

